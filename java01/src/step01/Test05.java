/* 주제: 정수리터럴과 2진수, 8진수, 10진수, 16진수
 */
package step01;

public class Test05 {

  public static void main(String[] args) {
  System.out.println(100); // 10진수
  System.out.println(0144); //8진수
  System.out.println(0x64); // 16진수
  System.out.println(0X64); // 16진수 대문자X를 써도되지만 소문자 x를 더많이 사용한다
  System.out.println(0xA9); // 16진수의 숫자에서 a~f 는 대 소문자 모두 가능
  System.out.println(0xa9);
  System.out.println(0b0110_0100);
  System.out.println(0B0110_0100); //2진수 소문자 b를 더많이 사용 
  //모두 숫자 100을 의미
  }
}
