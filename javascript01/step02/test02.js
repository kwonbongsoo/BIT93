/* 주제:비교 연산자
- ==, >, >=, <, <=, !=, ===
*/
"use strict"
var a = 7;
var b = 2;

console.log(a == b); //false
console.log(a > b); //true
console.log(a >= b); //true
console.log(a < b); //false
console.log(a <= b); //false
console.log(a != b); // true

console.log(10/0); //infinity
console.log(-10/0);//-infinity
